const replacerKeys = Object.keys(zhToEnMap).sort((a, b) => b.length - a.length)

function replacePreservingSpaces(source, translated) {
  const leftSpaces = source.match(/^\s*/)?.[0] ?? ''
  const rightSpaces = source.match(/\s*$/)?.[0] ?? ''
  return `${leftSpaces}${translated}${rightSpaces}`
}

export function translateByMap(text, language = 'zh-cn') {
  if (language !== 'en' || typeof text !== 'string' || !text.trim()) {
    return text
  }

  const exact = zhToEnMap[text.trim()]
  if (exact) {
    return replacePreservingSpaces(text, exact)
  }

  let translated = text
  for (const key of replacerKeys) {
    if (translated.includes(key)) {
      translated = translated.split(key).join(zhToEnMap[key])
    }
  }

  // Normalize mixed Chinese+English state labels, e.g. "未Inbound".
  translated = translated.replace(/未([A-Za-z][A-Za-z\s/-]*)/g, 'Pending $1')
  translated = translated.replace(/已([A-Za-z][A-Za-z\s/-]*)/g, 'Completed $1')
  translated = translated.replace(/待([A-Za-z][A-Za-z\s/-]*)/g, 'Pending $1')

  return translated
}

export default zhToEnMap
